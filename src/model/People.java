public class People {
    private String total;
    private String totalPepple;
    private String townPeople;
    private String urbanPeople;
    private String newPeople;
    private String diePeople;
    private String alwaysPeople;
    private String percent;

    public String getTotal() {
        return total;
    }

    public void setTotal(String total) {
        this.total = total;
    }

    public String getTotalPepple() {
        return totalPepple;
    }

    public void setTotalPepple(String totalPepple) {
        this.totalPepple = totalPepple;
    }

    public String getTownPeople() {
        return townPeople;
    }

    public void setTownPeople(String townPeople) {
        this.townPeople = townPeople;
    }

    public String getUrbanPeople() {
        return urbanPeople;
    }

    public void setUrbanPeople(String urbanPeople) {
        this.urbanPeople = urbanPeople;
    }

    public String getNewPeople() {
        return newPeople;
    }

    public void setNewPeople(String newPeople) {
        this.newPeople = newPeople;
    }

    public String getDiePeople() {
        return diePeople;
    }

    public void setDiePeople(String diePeople) {
        this.diePeople = diePeople;
    }

    public String getAlwaysPeople() {
        return alwaysPeople;
    }

    public void setAlwaysPeople(String alwaysPeople) {
        this.alwaysPeople = alwaysPeople;
    }

    public String getPercent() {
        return percent;
    }

    public void setPercent(String percent) {
        this.percent = percent;
    }
}
